# Operadoes aritmeticos en ruby

x = 5
y = 2

# Suma

suma = x + y

puts "Salida de la suma = #{suma}"

# Resta

resta = x - y

puts "Salida de la resta = #{resta}"

# Multiplicacion

multi = x * y

puts "Salida de la multiplicacion = #{multi}"

# Divicion

div = x/y

puts "Salida de la divicion = #{div}"
